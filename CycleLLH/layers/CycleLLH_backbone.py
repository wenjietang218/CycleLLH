class CycleLLH_backbone(nn.Module):
    def __init__(self, c_in:int, context_window:int, target_window:int, max_seq_len:Optional[int]=1024,
                 n_layers:int=3, d_model=128, n_heads=16, d_k:Optional[int]=None, d_v:Optional[int]=None,
                 d_ff:int=256, attn_dropout:float=0., dropout:float=0., act:str="gelu", key_padding_mask:bool='auto',
                 padding_var:Optional[int]=None, attn_mask:Optional[Tensor]=None, res_attention:bool=True, pre_norm:bool=False, store_attn:bool=False,
                 pe:str='zeros', learn_pe:bool=True, head_dropout = 0,
                 revin = True, affine = True, subtract_last = False,
                 weight_1=1, weight_2=1, C=24,
                 verbose:bool=False, **kwargs):
        
        super().__init__()

        self.weight_1 = weight_1  # 用于输入复制的次数
        self.weight_2 = weight_2  # 用于稠密神经网络复制的次数
        self.C = C   # 周期的长度
        self.period_num = int(context_window / self.C) # 输入的周期数

        # RevIn
        self.revin = revin
        if self.revin: self.revin_layer = RevIN(c_in, affine=affine, subtract_last=subtract_last)

        # clasify
        self.token_len = int(self.weight_1 * self.period_num) #输入到encoder的tokens的数量
        self.token_num = C                                    #输入到encoder的tokens的长度

        self.num = int((target_window + self.C - 1) / self.C) # 输出的周期数
        self.target_window = target_window
        self.context_window = context_window
        
        # Backbone
        self.backbone = TSTiEncoder(c_in, token_num=self.token_num, token_len=self.token_len, max_seq_len=max_seq_len,
                                n_layers=n_layers, d_model=d_model, n_heads=n_heads, d_k=d_k, d_v=d_v, d_ff=d_ff,
                                attn_dropout=attn_dropout, dropout=dropout, act=act, key_padding_mask=key_padding_mask, padding_var=padding_var,
                                attn_mask=attn_mask, res_attention=res_attention, pre_norm=pre_norm, store_attn=store_attn,
                                pe=pe, learn_pe=learn_pe, verbose=verbose, **kwargs)

        self.linears = nn.ModuleList([LinearHeads(int(d_model + self.weight_2 * self.period_num), 1) for i in range(self.num)])
        self.dropout = nn.Dropout(head_dropout)


    def forward(self, z):       # z: [bs x nvars x seq_len]
        # norm
        if self.revin:
            z = z.permute(0,2,1)
            z = self.revin_layer(z, 'norm')
            z = z.permute(0,2,1)

        # 周期整合 复制w1-1份并拼接后得到a1 复制w2-1份并拼接后得到a2
        xr1 = []  # 存储输入倍增到w1倍的张量
        xr2 = []  # 存储输入倍增到w2倍的张量

        for i in range(self.period_num):    #将输入序列按照周期C进行划分
            c = z[:, :, int(i * self.C): int((i + 1) * self.C)]
            c = torch.reshape(c, (c.shape[0], c.shape[1], c.shape[2], 1))
            xr1.append(c)  # xr1[i]: [bs x nvars x C x 1]
            xr2.append(c)

        for i in range(len(xr1)):
            c = xr1[i]
            for j in range(self.weight_1 - 1):
                c = torch.cat((c, xr1[i]), dim=-1)
            xr1[i] = c  # xr[i]: [bs x nvars x C x weight_1]
        x1 = torch.cat(xr1, dim=-1)  # x1: [bs x nvars x C x (weight_1*period_num)]

        for i in range(len(xr2)):
            c = xr2[i]
            for j in range(self.weight_2 - 1):
                c = torch.cat((c, xr2[i]), dim=-1)
            xr2[i] = c  # xr[i]: [bs x nvars x C x weight_2]
        x2 = torch.cat(xr2, dim=-1)   # x2:[bs x nvars x C x (weight_2*period_num)]

        # encoder主干网络
        x1 = x1.permute(0, 1, 3, 2)  # x1: [bs x nvars x (weight_1*period_num) x C]
        z = self.backbone(x1)  # z: [bs x nvars x d_model x C]
        z = z.permute(0, 1, 3, 2)   # z:[bs x nvars x C x d_model]

        # 稠密神经网络
        if self.weight_2 != 0:
            z = torch.cat((x2, z), dim=-1)  # z: [bs x nvars x C x (d_model + weight_2*period_num)]

        # 小线性层
        zr = []
        for i in range(self.num):
            t = self.linears[i](z)  # t : [bs x nvars x C x 1]
            t = torch.reshape(t, (t.shape[0], t.shape[1], t.shape[2]))
            zr.append(t)
        z = torch.cat(zr, dim=-1)
        z = z[:, :, :self.target_window]  # z : [bs x nvars x targetwinow]

        # denorm
        if self.revin:
            z = z.permute(0,2,1)
            z = self.revin_layer(z, 'denorm')
            z = z.permute(0,2,1)
        return z
    
    def create_pretrain_head(self, head_nf, vars, dropout):
        return nn.Sequential(nn.Dropout(dropout),
                    nn.Conv1d(head_nf, vars, 1)
                    )


class Flatten_Head(nn.Module):
    def __init__(self, individual, n_vars, nf, target_window, head_dropout=0):
        super().__init__()
        
        self.individual = individual
        self.n_vars = n_vars
        
        if self.individual:   #是否分开处理多元数据 channel-independence
            self.linears = nn.ModuleList()
            self.dropouts = nn.ModuleList()
            self.flattens = nn.ModuleList()
            for i in range(self.n_vars):
                self.flattens.append(nn.Flatten(start_dim=-2))
                self.linears.append(nn.Linear(nf, target_window))
                self.dropouts.append(nn.Dropout(head_dropout))
        else:
            self.flatten = nn.Flatten(start_dim=-2)
            self.linear = nn.Linear(nf, target_window)
            self.dropout = nn.Dropout(head_dropout)
            
    def forward(self, x):                                 # x: [bs x nvars x d_model x patch_num]
        if self.individual:
            x_out = []
            for i in range(self.n_vars):
                z = self.flattens[i](x[:,i,:,:])          # z: [bs x d_model * patch_num]
                z = self.linears[i](z)                    # z: [bs x target_window]
                z = self.dropouts[i](z)
                x_out.append(z)
            x = torch.stack(x_out, dim=1)                 # x: [bs x nvars x target_window]
        else:
            x = self.flatten(x)
            x = self.linear(x)
            x = self.dropout(x)
        return x
        
        
    
    
class TSTiEncoder(nn.Module):  #i means channel-independent
    def __init__(self, c_in, token_num, token_len, max_seq_len=1024,
                 n_layers=3, d_model=128, n_heads=16, d_k=None, d_v=None,
                 d_ff=256, norm='BatchNorm', attn_dropout=0., dropout=0., act="gelu", store_attn=False,
                 key_padding_mask='auto', padding_var=None, attn_mask=None, res_attention=True, pre_norm=False,
                 pe='zeros', learn_pe=True, verbose=False, **kwargs):
        
        
        super().__init__()
        
        self.token_num = token_num
        self.token_len = token_len
        
        # Input encoding
        q_len = token_num
        self.W_P = nn.Linear(token_len, d_model)        # Eq 1: projection of feature vectors onto a d-dim vector space
        self.seq_len = q_len

        # Positional encoding
        self.W_pos = positional_encoding(pe, learn_pe, q_len, d_model)

        # Residual dropout
        self.dropout = nn.Dropout(dropout)

        # Encoder
        self.encoder = TSTEncoder(q_len, d_model, n_heads, d_k=d_k, d_v=d_v, d_ff=d_ff, norm=norm, attn_dropout=attn_dropout, dropout=dropout,
                                   pre_norm=pre_norm, activation=act, res_attention=res_attention, n_layers=n_layers, store_attn=store_attn)

        
    def forward(self, x) -> Tensor:                                              # x: [bs x nvars x patch_len x patch_num]
        
        n_vars = x.shape[1]  #获取通道数目
        # Input encoding
        x = x.permute(0,1,3,2)                                                   # x: [bs x nvars x patch_num x patch_len]
        x = self.W_P(x)                                                          # x: [bs x nvars x patch_num x d_model]
            #将N*p的数据通过线性层 变为N*d_model的数据
        u = torch.reshape(x, (x.shape[0]*x.shape[1],x.shape[2],x.shape[3]))      # u: [bs * nvars x patch_num x d_model]
        u = self.dropout(u + self.W_pos)                                         # u: [bs * nvars x patch_num x d_model]
            #进行位置编码
        # Encoder
        z = self.encoder(u)                                                      # z: [bs * nvars x patch_num x d_model]
        z = torch.reshape(z, (-1,n_vars,z.shape[-2],z.shape[-1]))                # z: [bs x nvars x patch_num x d_model]
        z = z.permute(0,1,3,2)                                                   # z: [bs x nvars x d_model x patch_num]
        
        return z    
            
            
    
